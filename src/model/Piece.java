package model;

import javafx.scene.image.ImageView;

public abstract class Piece
{
	protected int x;
	protected int y;

	public Piece(int x, int y)
	{
		this.x = x;
		this.y = y;
	}

	public int getX()
	{
		return x;
	}

	public int getY()
	{
		return y;
	}

	public void setX(int x)
	{
		this.x = x;
	}

	public void setY(int y)
	{
		this.y = y;
	}
	
}
